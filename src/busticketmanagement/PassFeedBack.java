     private void displayInfo(){
     try {
    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
    Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");

    // Step 1: Find the schedule ID for the given passenger ID
    String schID = null;
    CallableStatement cstmt = conn.prepareCall("{call FindScheduleID(?)}");
    cstmt.setString(1, id); // Set parameter value
    ResultSet rs = cstmt.executeQuery();

    if (rs.next()) {
        // Retrieve the scheduleId value
        schID = rs.getString("ScheduleId");
    } else {
        // No schedule found for the passenger
        JOptionPane.showMessageDialog(this, "FIRST BOOK A TICKET AND TRAVEL", "Error", JOptionPane.ERROR_MESSAGE);
        PassBookBus bus = new PassBookBus(id);
        bus.setVisible(true);
        this.dispose();
        return; // Exit the method
    }

    // Step 2: Display information for feedback
    CallableStatement cstmt1 = conn.prepareCall("{call DisplayInfoForFeedback(?, ?)}");
    cstmt1.setString(1, id); // Set passenger ID
    cstmt1.setInt(2, Integer.parseInt(schID)); // Set schedule ID

    rs = cstmt1.executeQuery();

    // Check if the result set has any data
    if (rs.next()) {
        // Retrieve data from the result set
        String name = rs.getString("Name");
        String email = rs.getString("Email");
        String source = rs.getString("Source");
        String destination = rs.getString("Destination");
        String seatNumber = rs.getString("Seat_number");
        double fare = rs.getDouble("Fare");

        // Set the data to the text fields
        txt_name.setText(name);
        txt_email.setText(email);
        txt_source.setText(source);
        txt_dest.setText(destination);
        txt_seatNo.setText(seatNumber);
        txt_fare.setText(String.valueOf(fare));
    } else {
        // No data found for the passenger
        JOptionPane.showMessageDialog(this, "FIRST BOOK A TICKET AND TRAVEL", "Error", JOptionPane.ERROR_MESSAGE);
        btn_submit.setVisible(false);
        
    }

    // Close resources
    rs.close();
    cstmt.close();
    cstmt1.close();
    conn.close();
} catch (Exception ex) {
    ex.printStackTrace();
    // Handle SQL exception
    JOptionPane.showMessageDialog(this, "An error occurred while retrieving data", "Error", JOptionPane.ERROR_MESSAGE);
}

     }
