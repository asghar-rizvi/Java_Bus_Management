    class JpanelGradient extends JPanel{
        protected void paintComponent(Graphics g){
            Graphics2D g2d = (Graphics2D) g;
            int width = getWidth();
            int height= getHeight();
            
            Color color1 = new Color(52,143,80);
            Color color2 = new Color(86,180,211);
            GradientPaint gp= new GradientPaint(0,0,color1,180,height,color2);
            g2d.setPaint(gp);
            g2d.fillRect(0, 0, width, height);
        }
    }
    private void EnableManageButton(){
                String rank = "";
           try {
               // Load the SQL Server JDBC driver
               Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");

               try (Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");
                    CallableStatement cstmt = conn.prepareCall("{call ReturnRankOfAdmin(?)}")) {

                   cstmt.setString(1, username);

                   // Execute the stored procedure
                   try (ResultSet rs = cstmt.executeQuery()) {
                       // Check if there is a result
                       if (rs.next()) {
                           rank = rs.getString("AdminLevel");
                       }
                   }
               }
           } catch (Exception e) {
               // Handle exceptions
               JOptionPane.showMessageDialog(null, e.getMessage());
           }
           if (rank.equalsIgnoreCase("Super")){
               btn_manageAdmin.setVisible(true);
           }
           else
               btn_manageAdmin.setVisible(false);
    }
