    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String name = txt_adminName.getText();
        String pass = txt_password.getText();

        // Check if any field is empty
        if (name.isEmpty() || pass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
            
        } else {
            try {
                // Connect to the database
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                try (Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");
                     CallableStatement cstmtExist = conn.prepareCall("{call CheckIfAdminExist(?, ?)}");
                     CallableStatement cstmtInsert = conn.prepareCall("{call InsertIntoAdmin(?, ?,?)}")) {

                    // Check if the admin already exists
                    cstmtExist.setString(1, name);
                    cstmtExist.setString(2, pass);
                    ResultSet rs = cstmtExist.executeQuery();
                    rs.next();
                    int count = rs.getInt(1);
                    if (count > 0) {
                        JOptionPane.showMessageDialog(null, "Admin already exists!", "Error", JOptionPane.ERROR_MESSAGE);
                        txt_adminName.setText("");
                        txt_password.setText("");
                    } else {
                        // Insert the admin into the database
                        cstmtInsert.setString(1, name);
                        cstmtInsert.setString(2, pass);
                        cstmtInsert.setString(3, "Sub");
                        int rowsInserted = cstmtInsert.executeUpdate();
                        if (rowsInserted > 0) {
                            JOptionPane.showMessageDialog(null, "Admin added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(null, "Failed to add admin", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

