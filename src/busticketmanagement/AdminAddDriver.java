    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String driverid=txt_driverid.getText(), contact=txt_contact.getText(), name=txt_name.getText(),  lic=txt_lic.getText();
        String busID= (String) cmb_bs.getSelectedItem();
        if (driverid.isEmpty() || contact.isEmpty() || name.isEmpty() || lic.isEmpty() || cmb_bs.getSelectedIndex() ==0) {
            JOptionPane.showMessageDialog(this, "FIELDS ARE EMPTY");
        }
        else{
            try {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
             Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");
             CallableStatement cstmt = conn.prepareCall("{call insertIntoDriver(?,?,?,?,?)}");
             cstmt.setString(1, driverid);
             cstmt.setString(2, busID);
             cstmt.setString(3, name);
             cstmt.setString(4, lic);
             cstmt.setString(5, contact);
             
             int rowsInserted = cstmt.executeUpdate();
             if (rowsInserted>=0) {
                 JOptionPane.showMessageDialog(this, "SUCCESSFULLY INSERTED");
                 txt_driverid.setText("");
            txt_contact.setText("");
            txt_name.setText("");
            txt_lic.setText("");
            cmb_bs.setSelectedIndex(0);
            display();
                 
            }
             else{
                 JOptionPane.showMessageDialog(this, "ERROR");
             }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

