    private void txt_gmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_gmailFocusLost
        String email = txt_gmail.getText();

    // Check if the email meets the criteria
    if (email.length() >= 31 || !email.contains("@") || !email.endsWith(".com")) {
        // Show an error message to the user
        javax.swing.JOptionPane.showMessageDialog(null, "Email must be less than 31 characters, contain '@', and end with '.com'.");
        txt_gmail.requestFocus();
    } 
    }//GEN-LAST:event_txt_gmailFocusLost

