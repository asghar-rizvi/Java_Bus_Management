    public void display() {
            String connectionString = "jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true";
            try (Connection connection = DriverManager.getConnection(connectionString, "admin", "123456")) {
                String sql = "{call DisplayBus()}"; // Call the stored procedure
                try (CallableStatement statement = connection.prepareCall(sql)) {
                    try (ResultSet rs = statement.executeQuery()) {
                        // Create a DefaultTableModel
                        DefaultTableModel model = new DefaultTableModel();

                        // Add columns to the model (assuming column names match the result set)
                        model.addColumn("BusNumber");
                        model.addColumn("Color");
                        model.addColumn("Model");
                        model.addColumn("Capacity");

                        // Add rows to the model
                        while (rs.next()) {
                            model.addRow(new Object[]{
                                rs.getString("BusNumber"),
                                rs.getString("Color"),
                                rs.getString("Model"),
                                rs.getInt("Capacity")
                            });
                        }

                        table_display.setModel(model);
                        CustomHeaderRenderer renderer = new CustomHeaderRenderer();
                        for (int i = 0; i <=4; i++) {
          table_display.getColumnModel().getColumn(i).setHeaderRenderer(renderer);
            }
                    }
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }

}

