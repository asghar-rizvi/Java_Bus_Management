    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        if (cmb_rating.getSelectedIndex()==0 || txt_comm.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "FIELDS ARE EMPTY");
        }
        else{
            int rating = Integer.parseInt(cmb_rating.getSelectedItem().toString());
            String comments=txt_comm.getText();
            
            try {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");
        
                CallableStatement cstmt = conn.prepareCall("{call FindScheduleID(?)}");
                cstmt.setString(1, id); // Set parameter value
                ResultSet rs = cstmt.executeQuery();

                if (rs.next()) {
                    // Retrieve the scheduleId value
                    schID = rs.getString("ScheduleId");
                    
                }
                int schIDint= Integer.parseInt(schID);
                 try {
                    cstmt = conn.prepareCall("{call InserIntoFeedback(?, ?, ?, ?)}");
                    cstmt.setString(1, id); // Set passengerId parameter
                    cstmt.setInt(2, schIDint);      // Set ScheduleID parameter
                    cstmt.setInt(3, rating);          // Set rating parameter
                    cstmt.setString(4, comments);     // Set comments parameter
                    cstmt.executeUpdate();

        // Close resources
        cstmt.close();
        conn.close();
        
        JOptionPane.showMessageDialog(this, "Feedback submitted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        cmb_rating.setSelectedIndex(0);
        txt_comm.setText("");
        PassProfile profile = new PassProfile(id);
        profile.setVisible(true);
        this.dispose();
    } catch (Exception ex) {
        ex.printStackTrace();
        // Handle SQL exception
        JOptionPane.showMessageDialog(this, "An error occurred while submitting feedback", "Error", JOptionPane.ERROR_MESSAGE);
    }
                rs.close();
                cstmt.close();
                conn.close();
                
    } catch (Exception ex) {
        ex.printStackTrace();
        // Handle SQL exception
    }
            
            
        }
    }//GEN-LAST:event_btn_submitActionPerformed

