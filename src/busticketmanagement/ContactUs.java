    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
                String first = txt_first.getText().trim();
                String last = txt_last.getText().trim();
                String email = txt_gmail.getText().trim();
                String comm = txt_comments.getText().trim();

                if (first.isEmpty() || last.isEmpty() || email.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ContactUs.class.getName()).log(Level.SEVERE, null, ex);
        }
                // Call the stored procedure to insert values into ContactUs table
                try (Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");
                     CallableStatement stmt = conn.prepareCall("{call InsertIntoContactus(?, ?, ?, ?)}")) {

                    stmt.setString(1, first);
                    stmt.setString(2, last);
                    stmt.setString(3, email);
                    stmt.setString(4, comm);

                    stmt.executeUpdate();

                    JOptionPane.showMessageDialog(this, "Record inserted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

                    // Clear the text fields after successful insertion
                    txt_first.setText("");
                    txt_last.setText("");
                    txt_gmail.setText("");
                    txt_comments.setText("");

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Error inserting record: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            
        
    }//GEN-LAST:event_btn_submitActionPerformed

    private void txt_firstKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_firstKeyTyped
    String name = txt_first.getText();
    char keyChar = evt.getKeyChar();

    // Check if a key was pressed and it's not a backspace, delete, or space key
    if (keyChar != KeyEvent.VK_BACK_SPACE && keyChar != KeyEvent.VK_DELETE && keyChar != KeyEvent.VK_SPACE) {
        // Check if the entered character is not an alphabet
        if (!Character.isAlphabetic(keyChar)) {
            // Consume the event to prevent the character from being added
            evt.consume();
            // Show an error message to the user
            javax.swing.JOptionPane.showMessageDialog(null, "Please enter only alphabetic characters.");
            return;
        }  }      // TODO add your handling code here:
    }//GEN-LAST:event_txt_firstKeyTyped

