    private void btn_DELETEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DELETEActionPerformed
        if (table_Display.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "SELECT A ROW");
        } else {
            int selectedRowIndex = table_Display.getSelectedRow();
            if (selectedRowIndex != -1) {
                String col1Value = table_Display.getValueAt(selectedRowIndex, 0).toString();
                int driverid=Integer.parseInt(col1Value);
                try {
                    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                    try (Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");
                         CallableStatement cstmt = conn.prepareCall("{call DeleteDriverById (?)}")) {
                        cstmt.setInt(1, driverid);
                        int rows = cstmt.executeUpdate();
                        if (rows > 0) {
                            JOptionPane.showMessageDialog(this, "SUCCESSFULLY DELETED!!!");
                            display();
                        } else {
                            JOptionPane.showMessageDialog(this, "No rows deleted");
                        }
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btn_DELETEActionPerformed

