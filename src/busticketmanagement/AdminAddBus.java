    private void btn_inputBuhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inputBuhActionPerformed
        if (txt_busnumber.getText().isEmpty() || txt_capacity.getText().isEmpty() || txt_colorOfBus.getText().isEmpty() || txt_mode.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(this, "FIELDS ARE EMPTY");
        }
        else{
            try {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection conn = DriverManager.getConnection("jdbc:sqlserver://localhost\\SQLEXPRESS:1433;databaseName=mydatabase;encrypt=true;trustServerCertificate=true", "admin", "123456");
                CallableStatement cstmt = conn.prepareCall("{call AddBus(?,?,?,?)}");
                cstmt.setString(1,txt_busnumber.getText());
                cstmt.setString(2, txt_colorOfBus.getText());
                cstmt.setString(3, txt_mode.getText());
                cstmt.setInt(4, Integer.parseInt(txt_capacity.getText()));
                int rowsInserted = cstmt.executeUpdate();
                    if (rowsInserted > 0) {
                        JOptionPane.showMessageDialog(null, "Bus has been registered successfully!");
                        txt_busnumber.setText("");
                        txt_colorOfBus.setText("");
                        txt_mode.setText("");
                        txt_capacity.setText("");
                        display();
                        
                        } else {
                            JOptionPane.showMessageDialog(null, "FAILED DUE TO SOME ERROR");
                    }
                cstmt.close();
                conn.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btn_inputBuhActionPerformed

